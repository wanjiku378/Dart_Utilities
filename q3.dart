


void main() {
  String color = 'pink';

  switch (color) {
    case 'pink':
      print('You selected pink');
      break;

    case 'blue':
      print('You selected blue');
      break;

    case 'black':
      print('You selected black');
      break;

    case 'gray':
      print('You selected gray');
      break;

    case 'orange':
      print('You selected orange');
      break;

    case 'yellow':
      print('You selected Yellow');
      break;

    default:
      print('Unknown color!');
  }
}